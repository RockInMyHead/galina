def analyze_llm_response(response_text):
    """
    Analyze LLM response for repetition patterns like Sherlock Holmes
    """
    print("üîç –®–ï–†–õ–û–ö –•–û–õ–ú–°: –ê–ù–ê–õ–ò–ó –ü–û–í–¢–û–†–ï–ù–ò–ô –í –û–¢–í–ï–¢–ï LLM")
    print("=" * 60)

    # Split into sections
    sections = re.split(r'\d+\.\s+', response_text)[1:]  # Skip empty first element

    print(f"üìä –ù–∞–π–¥–µ–Ω–æ {len(sections)} —Å–µ–∫—Ü–∏–π –≤ –æ—Ç–≤–µ—Ç–µ")

    # Extract themes/topics from each section
    themes = []
    for i, section in enumerate(sections, 1):
        # Get first sentence or heading
        lines = section.strip().split('\n')
        theme = lines[0].strip() if lines else "–ë–µ–∑ —Ç–µ–º—ã"
        themes.append(theme)
        print(f"–°–µ–∫—Ü–∏—è {i}: {theme}")

    print("\nüîÑ –ê–ù–ê–õ–ò–ó –ü–û–í–¢–û–†–ï–ù–ò–ô:")

    # Find duplicate themes
    theme_counts = Counter(themes)
    duplicates = [theme for theme, count in theme_counts.items() if count > 1]

    if duplicates:
        print("üö® –û–ë–ù–ê–†–£–ñ–ï–ù–´ –ü–û–í–¢–û–†–Ø–Æ–©–ò–ï–°–Ø –¢–ï–ú–´:")
        for dup in duplicates:
            print(f"  - '{dup}' –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è {theme_counts[dup]} —Ä–∞–∑")
    else:
        print("‚úÖ –ü—Ä—è–º—ã—Ö –¥—É–±–ª–∏–∫–∞—Ç–æ–≤ —Ç–µ–º –Ω–µ –Ω–∞–π–¥–µ–Ω–æ")

    # Semantic similarity analysis
    print("\nüß† –°–ï–ú–ê–ù–¢–ò–ß–ï–°–ö–ò–ô –ê–ù–ê–õ–ò–ó –°–•–û–î–°–¢–í–ê:")

    # Prepare text for similarity analysis
    section_texts = [section.replace('\n', ' ') for section in sections]

    if len(section_texts) > 1:
        try:
            vectorizer = TfidfVectorizer(stop_words=stopwords.words('russian'))
            tfidf_matrix = vectorizer.fit_transform(section_texts)
            similarity_matrix = cosine_similarity(tfidf_matrix)

            print("–ú–∞—Ç—Ä–∏—Ü–∞ —Å—Ö–æ–∂–µ—Å—Ç–∏ —Å–µ–∫—Ü–∏–π:")
            for i in range(len(sections)):
                for j in range(i+1, len(sections)):
                    similarity = similarity_matrix[i][j]
                    if similarity > 0.7:  # High similarity threshold
                        print(f"–°–µ–∫—Ü–∏–∏ {i+1} –∏ {j+1} –æ—á–µ–Ω—å –ø–æ—Ö–æ–∂–∏ (—Å—Ö–æ–∂–µ—Å—Ç—å: {similarity:.3f})")
        except Exception as e:
            print(f"–ù–µ —É–¥–∞–ª–æ—Å—å –≤—ã–ø–æ–ª–Ω–∏—Ç—å —Å–µ–º–∞–Ω—Ç–∏—á–µ—Å–∫–∏–π –∞–Ω–∞–ª–∏–∑: {e}")
    # Extract key documents mentioned
    print("\nüìã –ê–ù–ê–õ–ò–ó –£–ü–û–ú–ò–ù–ê–ï–ú–´–• –î–û–ö–£–ú–ï–ù–¢–û–í:")

    all_docs = set()
    doc_patterns = [
        r'–∑–∞—è–≤–ª–µ–Ω–∏–µ –æ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏',
        r'—É—Å—Ç–∞–≤ –æ–æ–æ',
        r'–ø—Ä–æ—Ç–æ–∫–æ–ª —Å–æ–±—Ä–∞–Ω–∏—è',
        r'–∫–≤–∏—Ç–∞–Ω—Ü–∏—è.*–ø–æ—à–ª–∏–Ω—ã',
        r'–≥–∞—Ä–∞–Ω—Ç–∏–π–Ω–æ–µ –ø–∏—Å—å–º–æ',
        r'–¥–æ–≥–æ–≤–æ—Ä –∞—Ä–µ–Ω–¥—ã',
        r'—Ä–µ—à–µ–Ω–∏–µ –æ —Å–æ–∑–¥–∞–Ω–∏–∏'
    ]

    for section in sections:
        for pattern in doc_patterns:
            matches = re.findall(pattern, section.lower())
            if matches:
                all_docs.add(re.search(pattern, section.lower()).group())

    print(f"–£–Ω–∏–∫–∞–ª—å–Ω—ã–µ –¥–æ–∫—É–º–µ–Ω—Ç—ã: {sorted(all_docs)}")

    # Calculate repetition metrics
    total_words = sum(len(section.split()) for section in sections)
    unique_words = len(set(word.lower() for section in sections for word in section.split()))
    repetition_ratio = 1 - (unique_words / total_words) if total_words > 0 else 0

    print("\nüìà –ú–ï–¢–†–ò–ö–ò –ü–û–í–¢–û–†–ï–ù–ò–ô:")
    print(f"–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π —Å–ª–æ–≤: {repetition_ratio:.3f}")
    print(f"–í—Å–µ–≥–æ —Å–ª–æ–≤: {total_words}, —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Å–ª–æ–≤: {unique_words}")
    return {
        'sections_count': len(sections),
        'duplicate_themes': duplicates,
        'unique_documents': sorted(all_docs),
        'repetition_ratio': repetition_ratio,
        'similarity_matrix': similarity_matrix.tolist() if len(sections) > 1 else None
    }

